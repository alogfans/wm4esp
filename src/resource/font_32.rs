pub fn extract_font(ch: char) -> &'static [u8] {
    if ch.is_ascii_graphic() {
        let index = ch as u8 - ' ' as u8;
        &FONT_32_ASCII_MONOSPACE[index as usize]
    } else if ch == '°' {
        &FONT_32_EXTENDED_MONOSPACE[0]
    } else if FONT_32_CJK_DATASET.contains(ch) {
        for (idx, dst) in FONT_32_CJK_DATASET.chars().enumerate() {
            if dst == ch {
                return &FONT_32_CJK_MONOSPACE[idx];
            }
        }
        &FONT_32_ASCII_MONOSPACE[0]
    } else {
        &FONT_32_ASCII_MONOSPACE[0]
    }
}
